package ejercicio3_6;

public class Estudiante {
    String nombre;
    String apellidos;
    int codigo;
    int semestre;
    double nota;

    public Estudiante(String nombre, String apellidos, int codigo, int semestre, double nota) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.codigo = codigo;
        this.semestre = semestre;
        this.nota = nota;
    }
}
